def main() -> None:
    recipes = json.loads(RECIPES_PATH.read_text())
    by_id = {r["recipe_id"]: r for r in recipes}

    # Sanity: every recipe gets queries
    missing = [rid for rid in by_id if rid not in PER_RECIPE]
    if missing:
        raise RuntimeError(f"No queries for recipes: {missing}")

    extra = [rid for rid in PER_RECIPE if rid not in by_id]
    if extra:
        raise RuntimeError(f"Queries for nonexistent recipes: {extra}")

    OUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    qid = 0
    with OUT_PATH.open("w", encoding="utf-8") as f:
        for rid, r in by_id.items():
            ds = r["recipe"]["datasetId"]
            queries = PER_RECIPE[rid]
            for qtype in ("T1", "T2", "T3", "T4"):
                rec = {
                    "query_id": f"q_{qid:04d}",
                    "query": queries[qtype],
                    "current_dataset_id": ds,
                    "query_type": qtype,
                    "source_recipe_id": rid,
                }
                f.write(json.dumps(rec, ensure_ascii=False) + "\n")
                qid += 1

    print(f"Written {qid} queries to {OUT_PATH}")


if __name__ == "__main__":
    main()
